import sys

# ─── 첫째 마당: 숫자 읽기 ───────────────────────────────────────────────
# 한자식 수사(일, 이, 삼 ... 십, 백, 천, 만, 억, 조)를 실제 정수로 변환한다

일의자리 = {'일':1,'이':2,'삼':3,'사':4,'오':5,'육':6,'칠':7,'팔':8,'구':9}
소단위 = {'십':10,'백':100,'천':1000}          # 만 미만 단위
대단위 = {'만':10000,'억':100000000,'조':1000000000000}  # 만 이상 단위

def 정수읽기(낱말):
    """
    한자식 수사 낱말을 정수로 변환한다.
    예) '이십' → 20, '백삼십칠만오천' → 1375000
    변환 불가능하면 None을 돌려준다.
    """
    if 낱말 == '영':
        return 0
    결과 = 0          # 최종 결과값
    현재덩어리 = 0    # 현재 대단위(만/억/조) 앞에 쌓인 값
    현재값 = 0        # 현재 소단위 앞에 쌓인 일의자리 값
    for 글자 in 낱말:
        if 글자 in 일의자리:
            현재값 = 일의자리[글자]
        elif 글자 in 소단위:
            if 현재값 == 0:
                현재값 = 1  # '십' → '일십'으로 처리
            현재덩어리 += 현재값 * 소단위[글자]
            현재값 = 0
        elif 글자 in 대단위:
            현재덩어리 += 현재값
            if 현재덩어리 == 0:
                현재덩어리 = 1  # '만' → '일만'으로 처리
            결과 += 현재덩어리 * 대단위[글자]
            현재덩어리 = 0
            현재값 = 0
        else:
            return None  # 숫자가 아닌 글자가 나오면 실패
    현재덩어리 += 현재값
    결과 += 현재덩어리
    return 결과 if 결과 > 0 else None

def 숫자읽기(낱말):
    """정수읽기의 별칭. 값읽기 등에서 범용으로 사용한다."""
    return 정수읽기(낱말)


# ─── 둘째 마당: 낱말 나누기 ────────────────────────────────────────────
# 소스코드 문자열을 줄 단위로 쪼개어 (원본줄, 다듬은줄) 쌍의 목록으로 만든다
# 원본줄 → 들여쓰기 깊이 계산에 사용
# 다듬은줄 → 실제 명령 해석에 사용

def 낱말나누기(소스):
    """소스코드를 줄 목록으로 변환한다. 빈 줄은 건너뛴다."""
    줄목록 = []
    for 줄 in 소스.split('\n'):
        다듬음 = 줄.strip()
        if not 다듬음:
            continue
        줄목록.append((줄, 다듬음))
    return 줄목록


# ─── 셋째 마당: 풀이기 ─────────────────────────────────────────────────
# 파민정음 소스코드를 한 줄씩 읽어 실행하는 인터프리터 본체

class 정음풀이기:
    def __init__(self):
        self.변수 = {}       # 변수 이름 → 값 사전
        self.법목록 = {}     # 함수 이름 → (매개변수목록, 단락) 사전
        self.출력결과 = []   # 이르라로 출력된 문자열 목록
        self.오류목록 = []   # 실행 중 발생한 오류 메시지 목록

    def 오류(self, 메시지):
        """오류 메시지를 오류목록에 추가한다."""
        self.오류목록.append(f'아 슬프다 {메시지}')

    def 값읽기(self, 낱말):
        """
        낱말 하나를 값으로 변환한다.
        우선순위: 한자식 숫자 → 변수명 → 오류
        """
        번호 = 숫자읽기(낱말)
        if 번호 is not None:
            return 번호
        if 낱말 == '영':
            return 0
        if 낱말 in self.변수:
            return self.변수[낱말]
        self.오류(f'변수 {낱말} 가 존재하지 아니하옵니다')
        return 0

    def 말읽기(self, 낱말목록, 시작):
        """
        '이르되 ... 하니라' 형식의 문자열을 읽는다.
        부르되~하고: 변수 삽입 (f-string 역할)
        개행: 줄바꿈 문자로 변환
        돌려주는 값: (문자열값, 다음번호)
        """
        if 시작 >= len(낱말목록) or 낱말목록[시작] != '이르되':
            return None, 시작
        끝 = 시작 + 1
        while 끝 < len(낱말목록) and 낱말목록[끝] != '하니라':
            끝 += 1
        조각들 = []
        번호 = 시작 + 1
        while 번호 < 끝:
            낱말 = 낱말목록[번호]
            if 낱말 == '부르되':
                # 부르되 변수명 하고 → 변수 값을 문자열에 삽입
                번호 += 1
                if 번호 < 끝:
                    변수낱말 = 낱말목록[번호]
                    if 조각들 and 조각들[-1] not in ('\n',):
                        조각들.append(' ')
                    if 변수낱말 in self.변수:
                        조각들.append(str(self.변수[변수낱말]))
                    elif 숫자읽기(변수낱말) is not None:
                        조각들.append(str(숫자읽기(변수낱말)))
                    else:
                        조각들.append(변수낱말)
                    번호 += 1
                    if 번호 < 끝 and 낱말목록[번호] == '하고':
                        번호 += 1  # '하고' 건너뜀
                continue
            elif 낱말 == '개행':
                # 개행 → 줄바꿈 문자(\n)
                조각들.append('\n')
            else:
                if 조각들 and 조각들[-1] not in ('\n',):
                    조각들.append(' ')
                조각들.append(낱말)
            번호 += 1
        값 = ''.join(조각들)
        return 값, 끝 + 1

    def 연산읽기(self, 낱말목록, 번호):
        """
        '값 연산자 값' 형식의 사칙연산을 읽는다.
        연산자: 합하다(+) 감하다(-) 곱하다(*) 나누다(//) 남다(%)
        연산자가 없으면 왼쪽 값만 돌려준다.
        """
        if 번호 >= len(낱말목록):
            return 0, 번호
        왼쪽 = self.값읽기(낱말목록[번호])
        번호 += 1
        if 번호 >= len(낱말목록):
            return 왼쪽, 번호
        연산자 = 낱말목록[번호]
        if 연산자 not in ('합하다','감하다','곱하다','나누다','남다'):
            return 왼쪽, 번호
        번호 += 1
        if 번호 >= len(낱말목록):
            return 왼쪽, 번호
        오른쪽 = self.값읽기(낱말목록[번호])
        번호 += 1
        if 연산자 == '합하다': return 왼쪽 + 오른쪽, 번호
        if 연산자 == '감하다': return 왼쪽 - 오른쪽, 번호
        if 연산자 == '곱하다': return 왼쪽 * 오른쪽, 번호
        if 연산자 == '나누다':
            if 오른쪽 == 0:
                self.오류('영으로 나눌 수 없사옵니다')
                return 0, 번호
            return 왼쪽 // 오른쪽, 번호
        if 연산자 == '남다': return 왼쪽 % 오른쪽, 번호
        return 왼쪽, 번호

    def 조건판단(self, 조건낱말):
        """
        '변수 가 값 보다 작을/클/작거나같을/크거나같을'
        '변수 가 값 과 같을/같지않을' 형식의 조건을 판단한다.
        참이면 True, 거짓이면 False를 돌려준다.
        """
        if len(조건낱말) < 4:
            return False
        변수명 = 조건낱말[0]
        if 조건낱말[1] != '가':
            return False
        비교값 = self.값읽기(조건낱말[2])
        왼쪽값 = self.값읽기(변수명)
        if 조건낱말[3] == '보다' and len(조건낱말) >= 5:
            비교 = 조건낱말[4]
            if 비교 == '작을': return 왼쪽값 < 비교값
            if 비교 == '클': return 왼쪽값 > 비교값
            if len(조건낱말) >= 6 and 조건낱말[5] == '같을':
                if 비교 == '작거나': return 왼쪽값 <= 비교값
                if 비교 == '크거나': return 왼쪽값 >= 비교값
        if 조건낱말[3] == '과' and len(조건낱말) >= 5:
            비교 = 조건낱말[4]
            if 비교 == '같을': return 왼쪽값 == 비교값
            if 비교 == '같지않을': return 왼쪽값 != 비교값
        return False

    def 들여쓰기(self, 줄):
        """줄의 들여쓰기 깊이(공백 수)를 돌려준다. 블록 경계 판단에 사용한다."""
        return len(줄) - len(줄.lstrip())

    def 단락추출(self, 줄목록, 시작번호, 기준들여쓰기):
        """
        현재 블록에 속하는 줄들을 추출한다.
        끝이라, 아니하면 하되, 아니하고 만약을 만나면 멈춘다.
        돌려주는 값: (단락줄목록, 다음번호)
        """
        단락 = []
        번호 = 시작번호
        while 번호 < len(줄목록):
            원본줄, 다듬음 = 줄목록[번호]
            현재들여쓰기 = self.들여쓰기(원본줄)
            if 다듬음 in ('끝이라', '아니하면 하되') and 현재들여쓰기 <= 기준들여쓰기:
                break
            if 다듬음.startswith('아니하고 만약') and 현재들여쓰기 <= 기준들여쓰기:
                break
            단락.append((원본줄, 다듬음))
            번호 += 1
        return 단락, 번호

    def 실행(self, 줄목록, 지역변수=None):
        """
        줄목록을 한 줄씩 읽어 실행한다.
        지역변수: 함수 호출 시 매개변수를 전달하는 사전
        돌려주는 값: 바치다로 반환된 값 또는 None
        """
        원래변수 = None
        if 지역변수:
            # 함수 호출 시 지역 변수 공간 생성
            원래변수 = self.변수.copy()
            self.변수.update(지역변수)

        결과값 = None
        번호 = 0
        while 번호 < len(줄목록):
            원본줄, 줄 = 줄목록[번호]
            기준들여쓰기 = self.들여쓰기(원본줄)
            낱말목록 = 줄.split()
            번호 += 1

            if not 낱말목록:
                continue

            # 한 줄 주석: 사신은 논한다 내용
            if len(낱말목록) >= 2 and 낱말목록[0] == '사신은' and 낱말목록[1] == '논한다':
                continue

            # 여러 줄 주석: 사신은 논하되 ~ 논하기를 마치다
            if len(낱말목록) >= 2 and 낱말목록[0] == '사신은' and 낱말목록[1] == '논하되':
                while 번호 < len(줄목록):
                    _, 다음줄 = 줄목록[번호]
                    번호 += 1
                    if 다음줄.strip() == '논하기를 마치다':
                        break
                continue

            # 블록 끝 표시: 끝이라
            if 낱말목록[0] == '끝이라':
                continue

            # 정수 선언: 정수 나이는 이십
            if 낱말목록[0] == '정수' and len(낱말목록) >= 4 and 낱말목록[2] in ('는','은') and (len(낱말목록) < 5 or 낱말목록[3] != '행하라'):
                변수명 = 낱말목록[1]
                값, _ = self.연산읽기(낱말목록, 3)
                self.변수[변수명] = int(값)
                continue

            # 실수 선언: 실수 키는 백칠십사 점 오
            # 점 앞은 정수 덩어리, 점 뒤는 글자 하나씩 소수 자릿수로 읽는다
            if 낱말목록[0] == '실수' and len(낱말목록) >= 4 and 낱말목록[2] in ('는','은'):
                변수명 = 낱말목록[1]
                나머지 = 낱말목록[3:]
                if '점' in 나머지:
                    점번호 = 나머지.index('점')
                    정수부분 = 정수읽기(''.join(나머지[:점번호]))
                    소수부분 = 나머지[점번호+1:]
                    소수문자 = ''
                    for 낱말 in 소수부분:
                        for 글자 in 낱말:
                            글자값 = 일의자리.get(글자)
                            if 글자값 is not None:
                                소수문자 += str(글자값)
                            elif 글자 == '영':
                                소수문자 += '0'
                    if 정수부분 is None:
                        정수부분 = 0
                    값 = float(f'{정수부분}.{소수문자}')
                else:
                    정수값 = 정수읽기(''.join(나머지))
                    값 = float(정수값) if 정수값 is not None else 0.0
                self.변수[변수명] = 값
                continue

            # 말 선언: 말 이름은 이르되 홍길동 하니라
            if 낱말목록[0] == '말' and len(낱말목록) >= 4 and 낱말목록[2] in ('는','은'):
                변수명 = 낱말목록[1]
                값, _ = self.말읽기(낱말목록, 3)
                if 값 is not None:
                    self.변수[변수명] = 값
                else:
                    self.오류('말 선언 문법이 맞지 아니하옵니다')
                continue

            # 출력: 이르라 변수명 / 이르라 이르되 문자열 하니라
            if 낱말목록[0] == '이르라':
                if len(낱말목록) >= 2 and 낱말목록[1] == '이르되':
                    값, _ = self.말읽기(낱말목록, 1)
                    self.출력결과.append(str(값))
                elif len(낱말목록) >= 2:
                    변수명 = 낱말목록[1]
                    if 변수명 in self.변수:
                        self.출력결과.append(str(self.변수[변수명]))
                    else:
                        번호값 = 숫자읽기(변수명)
                        if 번호값 is not None:
                            self.출력결과.append(str(번호값))
                        else:
                            self.오류(f'변수 {변수명} 가 존재하지 아니하옵니다')
                continue

            # 횟수 반복: 되풀이 시작 부터 끝 전까지 [반복변수] 하되
            # 반복변수가 있으면 현재 인덱스 값이 해당 변수에 들어간다
            if 낱말목록[0] == '되풀이' and '부터' in 낱말목록 and '전까지' in 낱말목록 and 낱말목록[-1] == '하되':
                부터번호 = 낱말목록.index('부터')
                전까지번호 = 낱말목록.index('전까지')
                시작값 = self.값읽기(낱말목록[부터번호-1])
                끝값 = self.값읽기(낱말목록[전까지번호-1])
                반복변수 = None
                if 전까지번호 + 1 < len(낱말목록) - 1:
                    반복변수 = 낱말목록[전까지번호 + 1]
                단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                if 번호 < len(줄목록) and 줄목록[번호][1] == '끝이라':
                    번호 += 1
                for 현재값 in range(시작값, 끝값):
                    지역 = {반복변수: 현재값} if 반복변수 else {}
                    결과 = self.실행(단락, 지역)
                    if 결과 is not None:
                        결과값 = 결과
                        break
                continue

            # 조건 반복: 동안 조건 하되 ~ 끝이라
            # 조건이 참인 동안 단락을 반복 실행한다 (최대 10000회)
            if 낱말목록[0] == '동안' and 낱말목록[-1] == '하되':
                조건낱말 = 낱말목록[1:-1]
                단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                if 번호 < len(줄목록) and 줄목록[번호][1] == '끝이라':
                    번호 += 1
                반복횟수 = 0
                while self.조건판단(조건낱말):
                    반복횟수 += 1
                    if 반복횟수 > 10000:
                        self.오류('무한 동안이 감지되었사옵니다')
                        break
                    결과 = self.실행(단락)
                    if 결과 is not None:
                        결과값 = 결과
                        break
                continue

            # 조건문: 만약 ~ 아니하고 만약 ~ 아니하면 ~ 끝이라
            # if / elif / else 역할
            if 낱말목록[0] == '만약' and 낱말목록[-1] == '하되':
                구간목록 = []  # (조건낱말 또는 None, 단락) 쌍의 목록
                조건낱말 = 낱말목록[1:-1]
                단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                구간목록.append((조건낱말, 단락))
                # 아니하고 만약(elif), 아니하면(else) 연속 수집
                while 번호 < len(줄목록):
                    _, 다음줄 = 줄목록[번호]
                    다음낱말 = 다음줄.split()
                    if 다음낱말[0] == '아니하고' and len(다음낱말) > 1 and 다음낱말[1] == '만약' and 다음낱말[-1] == '하되':
                        번호 += 1
                        조건낱말 = 다음낱말[2:-1]
                        단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                        구간목록.append((조건낱말, 단락))
                    elif 다음줄 == '아니하면 하되':
                        번호 += 1
                        단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                        구간목록.append((None, 단락))  # None = else
                        break
                    else:
                        break
                if 번호 < len(줄목록) and 줄목록[번호][1] == '끝이라':
                    번호 += 1
                # 첫 번째로 참인 조건의 단락만 실행
                실행됨 = False
                for 조건, 단락 in 구간목록:
                    if 조건 is None:  # else
                        if not 실행됨:
                            결과 = self.실행(단락)
                            if 결과 is not None: 결과값 = 결과
                        break
                    if not 실행됨 and self.조건판단(조건):
                        결과 = self.실행(단락)
                        if 결과 is not None: 결과값 = 결과
                        실행됨 = True
                continue

            # 함수 선언: 함수 이름 [받되 매개변수...] 하되 ~ 끝이라
            if 낱말목록[0] == '함수' and 낱말목록[-1] == '하되':
                함수이름 = 낱말목록[1]
                받을것 = []
                if '받되' in 낱말목록:
                    받되번호 = 낱말목록.index('받되')
                    받을것 = 낱말목록[받되번호+1:-1]  # 매개변수 이름 목록
                단락, 번호 = self.단락추출(줄목록, 번호, 기준들여쓰기)
                if 번호 < len(줄목록) and 줄목록[번호][1] == '끝이라':
                    번호 += 1
                self.법목록[함수이름] = (받을것, 단락)  # 함수 등록
                continue

            # 함수 호출 후 반환값을 정수 변수에 저장:
            # 정수 결과는 행하라 함수명 [받되 인수...] [하되]
            if 낱말목록[0] == '정수' and len(낱말목록) >= 5 and 낱말목록[2] in ('는','은') and 낱말목록[3] == '행하라':
                변수명 = 낱말목록[1]
                함수이름 = 낱말목록[4]
                받음 = []
                if '받되' in 낱말목록:
                    받되번호 = 낱말목록.index('받되')
                    끝번호 = len(낱말목록) if 낱말목록[-1] != '하되' else len(낱말목록)-1
                    for 낱말 in 낱말목록[받되번호+1:끝번호]:
                        받음.append(self.값읽기(낱말))
                if 함수이름 in self.법목록:
                    받을것, 단락 = self.법목록[함수이름]
                    지역 = dict(zip(받을것, 받음))
                    원래출력 = self.출력결과[:]
                    결과 = self.실행(단락, 지역)
                    self.출력결과 = 원래출력
                    self.변수[변수명] = 결과 if 결과 is not None else 0
                else:
                    self.오류(f'함수 {함수이름} 이 존재하지 아니하옵니다')
                continue

            # 함수 호출 (반환값 없음): 행하라 함수이름 [받되 인수...] [하되]
            if 낱말목록[0] == '행하라':
                함수이름 = 낱말목록[1]
                받음 = []
                if '받되' in 낱말목록:
                    받되번호 = 낱말목록.index('받되')
                    끝번호 = len(낱말목록) if 낱말목록[-1] != '하되' else len(낱말목록)-1
                    for 낱말 in 낱말목록[받되번호+1:끝번호]:
                        받음.append(self.값읽기(낱말))
                if 함수이름 in self.법목록:
                    받을것, 단락 = self.법목록[함수이름]
                    지역 = dict(zip(받을것, 받음))
                    self.실행(단락, 지역)
                else:
                    self.오류(f'함수 {함수이름} 이 존재하지 아니하옵니다')
                continue

            # 함수 반환: 바치다 값
            # 현재 함수 실행을 멈추고 값을 돌려준다
            if 낱말목록[0] == '바치다' and len(낱말목록) >= 2:
                결과값 = self.값읽기(낱말목록[1])
                if 원래변수 is not None:
                    self.변수 = 원래변수
                return 결과값

            # 위 어느 문법에도 해당하지 않으면 오류
            self.오류(f'알 수 없는 문법이옵니다 하오니 {줄} {"을" if (ord(줄[-1]) - 44032) % 28 != 0 else "를"} 살펴보시옵소서')

        if 원래변수 is not None:
            self.변수 = 원래변수
        return 결과값


